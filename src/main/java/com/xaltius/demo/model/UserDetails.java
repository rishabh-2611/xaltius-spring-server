package com.xaltius.demo.model;

public class UserDetails {

	private String name;
	private int age;

	public UserDetails() {
		super();
	}

	// Getters & Setters
	// For Name
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	// For Age
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
